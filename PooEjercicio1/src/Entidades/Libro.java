
package Entidades;


public class Libro {
    
    public int isbn;
    public String titulo;
    public String autor;
    public int numPaginas;

    public Libro() {
    }

    public Libro(int isbn, String titulo, String autor, int numPaginas) {
        this.isbn = isbn;
        this.titulo = titulo;
        this.autor = autor;
        this.numPaginas = numPaginas;
    }
    
    
    
}
